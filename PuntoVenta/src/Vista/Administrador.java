            Tablatodo.addColumn("ID");
            Tablatodo.addColumn("Nombre");
            Tablatodo.addColumn("Dirección");
            Tablatodo.addColumn("Telefono");
            Tablatodo.addColumn("RFC");

            Object prov[][] = new Object[proveedor.size() / 5][5];
            int k = 0;
            for (int i = 0; i < proveedor.size() / 5; i++) {

                for (int j = 0; j < 5; j++) {
                    prov[i][j] = proveedor.get(k);
                    k++;
                }
                Tablatodo.addRow(prov[i]);
            }
            Tabla.setModel(Tablatodo);
