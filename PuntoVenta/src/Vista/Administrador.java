            Tablatodo.addColumn("ID");
            Tablatodo.addColumn("Nombres");
            Tablatodo.addColumn("Precio");
            Tablatodo.addColumn("Codigo");
            Tablatodo.addColumn("Cantidad");
            Tablatodo.addColumn("ID_Proveedor");

            Object prod[][] = new Object[producto.size() / 6][6];
