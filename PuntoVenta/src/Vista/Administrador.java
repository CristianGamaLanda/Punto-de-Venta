    private void actualizar() {
        Prod5.removeAllItems();
        Log4.removeAllItems();
        Prod5.addItem("Proveedor");
        Log4.addItem("Empleado");
        sql.Conexion();
        proveedor = sql.selectproveedor();

        for (int i = 0; i < proveedor.size(); i++) {
            Prod5.addItem(proveedor.get(i) + "-" + proveedor.get(i + 1));
            i++;
        }

        sql.Conexion();
        empleado = sql.selectempleado();
        for (int i = 0; i < empleado.size(); i++) {
            Log4.addItem(empleado.get(i) + "-" + empleado.get(i + 1) + " " + empleado.get(i + 2) + " " + empleado.get(i + 3));
            i += 3;
        }
    }

